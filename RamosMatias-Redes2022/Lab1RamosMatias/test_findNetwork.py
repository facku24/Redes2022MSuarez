from find_network import * 
import pytest

@pytest.mark.parametrize(
    "address, slash, expected",
    [
        ('25.34.12.56',     8,  ('25.0.0.0',        '')),
        ('128.11.3.31',     16, ('128.11.0.0',      '')),
        ('192.168.32.14',   24, ('192.168.32.0',    '')),
        ('202.44.82.16',    26, ('202.44.82.0',     '')),
        ('156.1.128.200',   20, ('156.1.128.0',     '')),
        ('199.199.199.172', 26, ('199.199.199.128', '')),
        ('45.11.130.250',   19, ('45.11.128.0',     '')),
        ('45.208.245.222',  13, ('45.208.0.0',      '')),
        ('192.141.27.224',  27, ('192.141.27.224',  '')),
        ('172.20.7.160',    26, ('172.20.7.128',    '')),
    ]
)
def test_find_network(address, slash, expected):
    assert find_network(address, slash) == expected
